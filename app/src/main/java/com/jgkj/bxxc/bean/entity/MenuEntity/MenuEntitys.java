public class MenuEntitys {

    private String package_id;

    private String packname;

    private String surplus_class;

    private String surplus_money;

    public String getPackage_id() {
        return package_id;
    }

    public void setPackage_id(String package_id) {
        this.package_id = package_id;
    }

    public String getPackname() {
        return packname;
    }

    public void setPackname(String packname) {
        this.packname = packname;
    }

    public String getSurplus_class() {
        return surplus_class;
    }

    public void setSurplus_class(String surplus_class) {
        this.surplus_class = surplus_class;
    }

    public String getSurplus_money() {
        return surplus_money;
    }

    public void setSurplus_money(String surplus_money) {
        this.surplus_money = surplus_money;
    }
}
